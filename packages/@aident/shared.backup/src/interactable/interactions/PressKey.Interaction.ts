import { KeyInput } from 'puppeteer-core';
import { z } from 'zod';
import { Base_Interaction, enforceIBaseInteractionStatic } from '~shared/interactable/interactions/Base.Interaction';
import { InteractableInteraction } from '~shared/messaging/action-configs/page-actions/types';

export class PressKey_Interaction extends Base_Interaction {
  public static readonly type = InteractableInteraction.PRESS_KEY;
  public static readonly description = 'Press a keyboard key on an element. Shortcut for KeyDown + KeyUp.';
  public static readonly configSchema = z.object({
    key: z.string().describe('Keyboard key to press.'),
    delay: z.number().optional().describe('Delay in milliseconds. Defaults to 0.'),
  });

  public async exec(config?: z.infer<typeof PressKey_Interaction.configSchema>): Promise<void> {
    const { key, delay } = PressKey_Interaction.configSchema.parse(config);
    if (!key) throw new Error('Key is required for Press interaction');
    if (!KeyInputAllowedValues.has(key)) throw new Error(`Key ${key} is not supported`);

    const delayConfig = delay ? { delay } : undefined;
    await this.handle.press(key as KeyInput, delayConfig);
  }
}

